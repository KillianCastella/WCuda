__device__ double fpi(double x)
    {
    return 4 / (1 + x * x);
    }

__global__ void slice(float* ptrDevTab, int n)
    {
    const int NB_THREAD = Indice2D::nbThread();
    const int TID = Indice2D::tid();
	{
	const double DX = 1 / (double) n;
	double xs;
	int s = TID;
	while (s < n)
	    {
	    xs = s * DX;
	    ptrDevTab[s] = fpi(xs);
	    s += NB_THREAD;
	    }
	}
    }



